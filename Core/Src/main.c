void pca9548_select(uint8_t channel) {
    uint8_t data = (1 << channel);
    HAL_I2C_Master_Transmit(&hi2c1, PCA9548A_ADDR << 1, &data, 1, 100);
    HAL_Delay(1);
}

int __io_putchar(int ch)
{
  HAL_UART_Transmit(&hlpuart1, (uint8_t *)&ch, 1, 0xFFFF);
  return ch;
}

// Silnik A (TIM1: PA8=CH1, PA11=CH4)
void motorA(int16_t speed) {
    if (speed > 0) {
        TIM1->CCR1 = speed;
        TIM1->CCR4 = 0;
    } else if (speed < 0) {
        TIM1->CCR1 = 0;
        TIM1->CCR4 = -speed;
    } else {
        TIM1->CCR1 = 0;
        TIM1->CCR4 = 0;
    }
}

// Silnik B (TIM2: PA15=CH1, PB3=CH2)
void motorB(int16_t speed) {
    if (speed > 0) {
        TIM2->CCR1 = speed;
        TIM2->CCR2 = 0;
    } else if (speed < 0) {
        TIM2->CCR1 = 0;
        TIM2->CCR2 = -speed;
    } else {
        TIM2->CCR1 = 0;
        TIM2->CCR2 = 0;
    }
}
